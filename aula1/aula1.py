# Condicionais: If


nome = 'Thiago'

if nome == 'Thiago':
    print('Nome correto')
else:
    print('Nome incorreto')

idade = int(input('Digite sua idade'))

if idade >= 18:
    print('Acesso permitido')
else: 
    print('Acesso negado')


numero = 10
